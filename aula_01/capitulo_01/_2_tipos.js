//______________ 1. Linguagem dinamicamente tipada

// As variáveis não têm um tipo fixo definido no momento da declaração. Em vez disso, o tipo da variável é determinado em tempo de execução, quando o programa está sendo executado.

// Características de Linguagens Dinamicamente Tipadas
// 1. Tipos Flexíveis:
// Você pode atribuir valores de diferentes tipos a uma variável ao longo do tempo. Caso ela seja declarada como let, o valor dela pode mudar de 2 para "batata" ou { nome: "João", idade: 30 }.

// 2. Sem Declaração de Tipo:
// Você não precisa especificar o tipo de uma variável ao declará-la. O sistema de tipos determina automaticamente o tipo da variável com base no valor que ela recebe (em tempo de execução).

// 3. Verificação de Tipos em Tempo de Execução:
// Os tipos são verificados apenas quando o programa é executado. Isso pode levar a erros que não são detectados até que a parte do código que usa a variável seja executada.

// 4. Maior Flexibilidade:
// Linguagens dinamicamente tipadas oferecem mais flexibilidade e rapidez para escrever código, pois você não precisa se preocupar em declarar tipos explícitos.


//______________ 2. Tipos existentes
